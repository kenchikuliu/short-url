export const resolveLanguage = (language) => {
  if (!language) {
    return DEFAULT_LANGUAGE;
  }

  if (SUPPORTED_LANGUAGES.includes(language)) {
    return language;
  }

  const normalized = language.toLowerCase();
  const matched = SUPPORTED_LANGUAGES.find((candidate) => {
    const candidateNormalized = candidate.toLowerCase();
    return (
      candidateNormalized === normalized ||
      candidateNormalized.startsWith(normalized + "-") ||
      normalized.startsWith(candidateNormalized + "-")
    );
  });

  return matched || DEFAULT_LANGUAGE;
};

export const getCopy = (language) => copy[resolveLanguage(language)];
